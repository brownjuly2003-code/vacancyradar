@lru_cache(maxsize=4)
def _load_city_index(path_str: str) -> dict[str, str]:
    raw = yaml.safe_load(Path(path_str).read_text(encoding="utf-8")) or {}
    index: dict[str, str] = {}
    for district, cities in raw.items():
        for city in cities or []:
            index.setdefault(city.strip().lower(), district)
    return index


def _oblast_key(parens_content: str) -> str | None:
    """Из «Кировская область» / «ХМАО — Югра» / «Республика Татарстан»
    извлечь нормализованный ключ для `_OBLAST_TO_DISTRICT`.
    """
    text = parens_content.lower().strip()
    # отрезать суффиксы типов субъекта
    text = re.sub(
        r"\s+(область|край|республика|автономн[ыа]я\s+область|"
        r"автономн[ыа]й\s+округ|ао|округ|г\.?)$",
        "",
        text,
    )
    text = re.sub(r"^республика\s+", "", text)
    return text.strip() or None


def region_for_city(
    city: str | None,
    regions_path: Path = DEFAULT_REGIONS_PATH,
) -> str | None:
    if not city:
        return None
    raw = city.strip()
    if not raw:
        return None

    cities = _load_city_index(str(regions_path))

    # Strip админ-префикс «городской округ N» / «город N».
    cleaned = _PREFIX_RE.sub("", raw).strip()

    # Прямое совпадение по полному имени.
    if (hit := cities.get(cleaned.lower())):
        return hit

    # Hh.ru формат: "Киров (Кировская область)".
    if (m := _PARENS_RE.search(cleaned)):
        bare = _PARENS_RE.sub("", cleaned).strip()
        if bare and (hit := cities.get(bare.lower())):
            return hit
        oblast = _oblast_key(m.group(1))
        if oblast and (hit := _OBLAST_TO_DISTRICT.get(oblast)):
            return hit

    return None
